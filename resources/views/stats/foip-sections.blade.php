<div class="foip-section" data-foip-number="unknown">
    <h3>Other section of FOIP</h3>
    <p>
        We've provided a brief summary of the most common FOIP sections here, but you can view them all in the official
        FOIP act on the <a href="http://www.qp.alberta.ca/documents/Acts/F25.pdf">Queen's Printer</a> (pdf)
    </p>
</div>

<div class="foip-section" data-foip-number="16">
    <h3>FOIP Section 16: Disclosure harmful to business interests of a third party</h3>
    <p>
        If a third-party business provides information, in confidence, to the City of Edmonton, they can use this section
        to prevent any of that information from being revealed to the public during a public meeting, with the intention
        of protecting a business' economic interests.
    </p>
</div>

<div class="foip-section" data-foip-number="17">
    <h3>FOIP Section 17: Disclosure harmful to personal privacy</h3>
    <p>
        If a third-party business provides information, in confidence, to the City of Edmonton, they can use this section
        to prevent any of that information from being revealed to the public during a public meeting, with the intention
        of protecting a business' economic interests.
    </p>
</div>

<div class="foip-section" data-foip-number="19">
    <h3>FOIP Section 19: Confidential evaluations</h3>
    <p>
        When City Council performs agency and board interviews as well as employee and other types of evaluations, the details
        of such evaluations are kept private under this section.
    </p>
</div>

<div class="foip-section" data-foip-number="21">
    <h3>FOIP Section 21: Disclosure harmful to intergovernmental relations</h3>
    <p>
        Any information that might be expected to harm and embarrass relations between orders of governments or government agencies.
        Much, I'm sure, to WikiLeaks' chagrin.
    </p>
</div>


<div class="foip-section" data-foip-number="24" style="display:block;">
    <h3>FOIP Section 24: Advice from Officials</h3>
    <p>
        This section of FOIP primarily deals with keeping the advice and guidance of government officials private. Since this section
        permits the keeping private <em>positions, plans, procedures, criteria or instructions
            developed for the purpose of contractual or other
            negotiations by or on behalf of the Government of Alberta
            or a public body, or considerations that relate to those
            negotiations</em>
        it was often used in relation to LRT projects.
    </p>
</div>

<div class="foip-section" data-foip-number="25">
    <h3>FOIP Section 25: Disclosure harmful to economic and other interests of a public body</h3>
    <p>
        This section of FOIP broadly gives the City the authority to deny access to information based on the perceived monetary
        value of that information and the potential competitive advantage the city <em>might</em> lose by releasing said information.
    </p>
</div>

<div class="foip-section" data-foip-number="27">
    <h3>FOIP Section 27: Privileged information</h3>
    <p>
        This section deals with protecting information that is privileged either through solicitor-client privilege or parliamentary privilege.
        Of important note, according to this section the city <strong>must</strong> refuse to release information under
        solicitor-client privilege that relates to any person that is not a public body. Any legal matters relating to
        individual citizens or businesses must not be released.
    </p>
</div>
